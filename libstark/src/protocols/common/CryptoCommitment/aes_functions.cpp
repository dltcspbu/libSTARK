inline void ShiftRows(uint8_t* a) {
    uint8_t* temp = new uint8_t[16];
    std::memcpy(temp, a, 16);
    for (int i = 0; i < 4; ++i) {
        for(int j = 0; j < 4; ++j) {
            a[4 * i + j] = temp[4 * ((i + j) % 4) + j];
        }
    }
    delete[] temp;
}

inline void SubBytes(uint8_t* a) {
	for (int i = 0; i < 16; ++i) {
		a[i] = Sbox[a[i]];
	}
}

// source: https://en.wikipedia.org/wiki/Rijndael_MixColumns#Implementation_example
inline void gmix_column(uint8_t* r) {
    uint8_t a[4];
    uint8_t b[4];
    uint8_t c;
    uint8_t h;
    /* The array 'a' is simply a copy of the input array 'r'
     * The array 'b' is each element of the array 'a' multiplied by 2
     * in Rijndael's Galois field
     * a[n] ^ b[n] is element n multiplied by 3 in Rijndael's Galois field */ 
    for (c = 0; c < 4; c++) {
        a[c] = r[c];
        /* h is 0xff if the high bit of r[c] is set, 0 otherwise */
        h = (uint8_t)((signed char)r[c] >> 7); /* arithmetic right shift, thus shifting in either zeros or ones */
        b[c] = r[c] << 1; /* implicitly removes high bit because b[c] is an 8-bit char, so we xor by 0x1b and not 0x11b in the next line */
        b[c] ^= 0x1B & h; /* Rijndael's Galois field */
    }
    r[0] = b[0] ^ a[3] ^ a[2] ^ b[1] ^ a[1]; /* 2 * a0 + a3 + a2 + 3 * a1 */
    r[1] = b[1] ^ a[0] ^ a[3] ^ b[2] ^ a[2]; /* 2 * a1 + a0 + a3 + 3 * a2 */
    r[2] = b[2] ^ a[1] ^ a[0] ^ b[3] ^ a[3]; /* 2 * a2 + a1 + a0 + 3 * a3 */
    r[3] = b[3] ^ a[2] ^ a[1] ^ b[0] ^ a[0]; /* 2 * a3 + a2 + a1 + 3 * a0 */
}

inline void MixColumns(uint8_t* a) {
	for (int i = 0; i < 4; ++i) {
		gmix_column(a + 4 * i);
	}
}

inline uint SubWord(uint a) {
	uint8_t* byte = (uint8_t*)&a;
	for (int i = 0; i < 4; ++i) {
		byte[i] = Sbox[byte[i]];
	}
	return a;
}

inline uint RotWord(uint a) {
	uint8_t* byte = (uint8_t*)&a;
	uint8_t temp = byte[0];
	byte[0] = byte[1];
	byte[1] = byte[2];
	byte[2] = byte[3];
	byte[3] = temp;
	return a;
}

inline void _My_aesenc_si128(uint8_t* a, const uint8_t* RoundKey) {
    ShiftRows(a);
	SubBytes(a);
	MixColumns(a);
    ((uint64_t*)a)[0] ^= ((uint64_t*)RoundKey)[0];
    ((uint64_t*)a)[1] ^= ((uint64_t*)RoundKey)[1];
}

inline void _My_aesenclast_si128(uint8_t* a, const uint8_t* RoundKey) {
    ShiftRows(a);
	SubBytes(a);
    ((uint64_t*)a)[0] ^= ((uint64_t*)RoundKey)[0];
    ((uint64_t*)a)[1] ^= ((uint64_t*)RoundKey)[1];
}

inline void _My_aeskeygenassist_si128(uint8_t* a, const int imm8, uint8_t* dst) {
	uint* X = (uint*)a;
	uint RCON = imm8;
	uint* dst_uint = (uint*)dst;
	dst_uint[0] = SubWord(X[1]);
	dst_uint[1] = RotWord(SubWord(X[1])) xor RCON;
	dst_uint[2] = SubWord(X[3]);
	dst_uint[3] = RotWord(SubWord(X[3])) xor RCON;
}

